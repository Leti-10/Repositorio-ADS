const personagens=[Personagem, Personagem2, Personagem3, Personagem4, Personagem5, Personagem6, Personagem7, Personagem8, Personagem9]
const criterio = [
        {chave:"forca", nome:"Força"},
        {chave:"velocidade", nome:"Velocidade"},
        {chave:"resistencia", nome:"Resistência"}
    ]; 

const ranking = {};
personagens.forEach(p => ranking[p.codinome] = 0);

function comparar(personagens) {
    for(let i=0; i<personagens.length; i++){
        for(let j=i+1; j<personagens.length; j++){
            let persona1 = personagens[i];
            let persona2 = personagens[j];
            let pontos1 = 0;
            let pontos2 = 0;

            console.log(`\n${persona1.codinome} VS ${persona2.codinome}\n`);

            criterio.forEach(c => {
                if (persona1[c.chave] > persona2[c.chave]) {
                    console.log(`${c.nome}: ${persona1.codinome} vence (${persona1[c.chave]} > ${persona2[c.chave]})`);
                    pontos1++;
                } else if (persona2[c.chave] > persona1[c.chave]) {
                    console.log(`${c.nome}: ${persona2.codinome} vence (${persona2[c.chave]} > ${persona1[c.chave]})`);
                    pontos2++;
                } else {
                    console.log(`${c.nome}: Empate (${persona1[c.chave]} = ${persona2[c.chave]})`);
                }
            });

            if (pontos1 > pontos2) {
                console.log(`Vencedor do duelo: ${persona1.codinome}`);
                ranking[persona1.codinome]++;
            } else if (pontos2 > pontos1) {
                console.log(`Vencedor do duelo: ${persona2.codinome}`);
                ranking[persona2.codinome]++;
            } else {
                console.log("Empate geral do duelo!");
            }
        }
    }


    let vencedorGeral = "";
    let maior = -1;

    for (let personagem in ranking) {
        if (ranking[personagem] > maior) {
            maior = ranking[personagem];
            vencedorGeral = personagem;
        }
    }
    console.log("\n VENCEDOR GERAL:");
    console.log(` ${vencedorGeral}`);
}


comparar(personagens);
